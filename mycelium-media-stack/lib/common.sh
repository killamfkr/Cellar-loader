      retries: 3
      start_period: 45s

  plex:
    image: lscr.io/linuxserver/plex:latest
    container_name: plex
    restart: unless-stopped
    networks:
      - media-network
    ports:
      - "${PLEX_BIND:-0.0.0.0}:32400:32400"
    environment:
      PUID: ${PUID:-1000}
      PGID: ${PGID:-1000}
      TZ: ${TZ:-UTC}
      VERSION: docker
      PLEX_CLAIM: ${PLEX_CLAIM:-}
    depends_on:
      mycelium:
        condition: service_healthy
    volumes:
      - ${CONFIG_DIR}/plex:/config
      - ${DATA_DIR}/plex-media:/plex-media:ro
      - ${DATA_DIR}/spore:/spore:ro
    entrypoint:
      - /bin/sh
      - -c
      - |
        set -e
