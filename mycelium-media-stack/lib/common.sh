    volumes:
      - ${CONFIG_DIR}/seerr:/app/config
    depends_on:
      - plex
      - radarr
      - sonarr

EOF
}

apply_bind_addresses() {
  local env_file="$1"
  local expose_lan="${EXPOSE_LAN:-true}"
  local bind="0.0.0.0"
  if [[ "${expose_lan}" != "true" ]]; then
    bind="127.0.0.1"
  fi
  {
    echo "MYCELIUM_BIND=${bind}"
    echo "PLEX_BIND=${bind}"
    echo "PROWLARR_BIND=${bind}"
    echo "BYPARR_BIND=${bind}"
    echo "RADARR_BIND=${bind}"
    echo "SONARR_BIND=${bind}"
    echo "SEERR_BIND=${bind}"
  } >>"${env_file}"
}

print_post_install() {
  local install_dir="$1"
  local host_ip="$2"

  cat <<EOF

${GREEN}Install complete.${NC}

Install directory: ${install_dir}
Manage stack:      cd ${install_dir} && ./manage.sh <command>

Service URLs (LAN):
  Mycelium (setup):  http://${host_ip}:8088
  Plex:              http://${host_ip}:32400/web
  Seerr:             http://${host_ip}:5055
  Radarr:            http://${host_ip}:7878
  Sonarr:            http://${host_ip}:8989
  Prowlarr:          http://${host_ip}:9696

Next steps:
  1. Open Mycelium and finish the setup wizard (TorBox + TMDB keys, Catbox mode).
  2. Claim Plex (optional): export PLEX_CLAIM=... && ./manage.sh claim-plex && ./manage.sh restart plex
  3. In Plex, add a library pointing at /plex-media (Movies + TV).
  4. In Seerr: connect Plex, Radarr, Sonarr; set webhook to http://mycelium:8088/webhook
  5. In Mycelium Admin: add Radarr/Sonarr URLs for bulk import (not download clients).

Notes:
  - Mycelium handles grabs/streaming. Radarr/Sonarr are for bulk import + Seerr routing.
  - Spore is experimental; Linux/desktop Plex clients work best.
  - Only stream content you have the right to access.

EOF
}
