#!/usr/bin/env bash
# Shared helpers for ZimaOS and Unraid installers.

set -euo pipefail

STACK_NAME="mycelium-media-stack"
MYCELIUM_REPO="https://github.com/corveck79/mycelium.git"
MYCELIUM_IMAGE="ghcr.io/corveck79/mycelium:latest"

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
CYAN='\033[0;36m'
NC='\033[0m'

log_info()  { echo -e "${GREEN}[INFO]${NC} $*" >&2; }
log_warn()  { echo -e "${YELLOW}[WARN]${NC} $*" >&2; }
log_error() { echo -e "${RED}[ERROR]${NC} $*" >&2; }
log_step()  { echo -e "${CYAN}==>${NC} $*" >&2; }

require_cmd() {
  local cmd
  for cmd in "$@"; do
    if ! command -v "$cmd" &>/dev/null; then
      log_error "Required command not found: ${cmd}"
      exit 1
    fi
  done
}

detect_host_ip() {
  local ip=""
  ip="$(hostname -I 2>/dev/null | awk '{print $1}')" || true
  if [[ -z "${ip}" ]]; then
    ip="$(ip -4 route get 1.1.1.1 2>/dev/null | awk '{for (i=1;i<=NF;i++) if ($i=="src") {print $(i+1); exit}}')" || true
  fi
  if [[ -z "${ip}" ]]; then
    ip="127.0.0.1"
    log_warn "Could not detect LAN IP; using 127.0.0.1 for CATBOX_HOST. Update .env after install."
  fi
  echo "${ip}"
}

detect_puid_pgid() {
  local puid pgid
  puid="${PUID:-$(id -u)}"
  pgid="${PGID:-$(id -g)}"
  echo "${puid}:${pgid}"
}

random_secret() {
  if command -v openssl &>/dev/null; then
    openssl rand -hex 16
  else
    tr -dc 'a-f0-9' </dev/urandom | head -c 32
  fi
}

ensure_dir() {
  local dir="$1"
  mkdir -p "${dir}"
  chown -R "${PUID:-1000}:${PGID:-1000}" "${dir}" 2>/dev/null || true
}

download_spore_wrapper() {
  local dest_dir="$1"
  local wrapper="${dest_dir}/plex_transcoder_wrapper.sh"
  mkdir -p "${dest_dir}"
  curl -fsSL "https://raw.githubusercontent.com/corveck79/mycelium/main/spore/plex_transcoder_wrapper.sh" \
    -o "${wrapper}.upstream"
  # Upstream hardcodes 127.0.0.1; Plex runs in a separate container on bridge networks.
  sed 's|http://127.0.0.1:8088|http://mycelium:8088|g' "${wrapper}.upstream" > "${wrapper}"
  rm -f "${wrapper}.upstream"
  chmod +x "${wrapper}"
}

write_env_file() {
  local env_file="$1"
  local host_ip="$2"
  local webhook_secret="$3"

  if [[ -f "${env_file}" ]]; then
    log_warn "${env_file} already exists; leaving it untouched."
    return 0
  fi

  cat >"${env_file}" <<EOF
# Generated by ${STACK_NAME} installer on $(date -u +%Y-%m-%dT%H:%M:%SZ)
PUID=${PUID:-1000}
PGID=${PGID:-1000}
TZ=${TZ:-UTC}

# Required: paste keys before first use (or complete the Mycelium setup wizard).
TORBOX_API_KEY=${TORBOX_API_KEY:-replace-me}
TMDB_API_KEY=${TMDB_API_KEY:-replace-me}

# Mycelium / Catbox (lazy TorBox materialization — CatBox-style behavior)
CATBOX_MODE=true
CATBOX_HOST=http://${host_ip}:8088
CATBOX_IDLE_MINUTES=43200

# Mycelium Spore (Plex stub library)
SPORE_ENABLED=true
SPORE_MEDIA_PATH=/data/plex-media

# Optional Seerr integration (configure webhook after install)
SEERR_URL=http://seerr:5055
SEERR_API_KEY=

# Webhook secret for Seerr -> Mycelium
WEBHOOK_SECRET=${webhook_secret}

# Plex claim token (optional; expires in ~4 minutes) https://www.plex.tv/claim/
PLEX_CLAIM=${PLEX_CLAIM:-}

# Paths (installer sets these for your platform)
CONFIG_DIR=${CONFIG_DIR}
DATA_DIR=${DATA_DIR}

# Expose services on LAN (set false to bind localhost only)
EXPOSE_LAN=${EXPOSE_LAN:-true}

COMPOSE_PROJECT_NAME=${STACK_NAME}
EOF
}

write_compose_file() {
  local compose_file="$1"
  cat >"${compose_file}" <<'EOF'
# Mycelium + Spore + Plex + *arr stack
# Managed by install-zimaos.sh / install-unraid.sh

networks:
  media-network:
    driver: bridge

services:
  mycelium:
    image: ghcr.io/corveck79/mycelium:latest
    container_name: mycelium
    restart: unless-stopped
    networks:
      - media-network
    ports:
      - "${MYCELIUM_BIND:-0.0.0.0}:8088:8088"
    env_file:
      - .env
    environment:
      LISTEN_HOST: 0.0.0.0
      LISTEN_PORT: 8088
      MEDIA_PATH: /data/media
      DB_PATH: /data/requests.db
      SPORE_ENABLED: ${SPORE_ENABLED:-true}
      SPORE_MEDIA_PATH: ${SPORE_MEDIA_PATH:-/data/plex-media}
      CATBOX_MODE: ${CATBOX_MODE:-true}
      CATBOX_HOST: ${CATBOX_HOST}
      WEBHOOK_SECRET: ${WEBHOOK_SECRET}
      TORBOX_API_KEY: ${TORBOX_API_KEY}
      TMDB_API_KEY: ${TMDB_API_KEY}
      SEERR_URL: ${SEERR_URL:-}
      SEERR_API_KEY: ${SEERR_API_KEY:-}
