      - ${DATA_DIR}/mycelium:/data
      - ${DATA_DIR}/plex-media:/data/plex-media
      - ${DATA_DIR}/spore:/spore:ro
    devices:
      - /dev/dri:/dev/dri
    group_add:
      - "44"
      - "109"
    healthcheck:
      test:
        [
          "CMD",
          "python",
          "-c",
          "import urllib.request,sys; r=urllib.request.urlopen('http://127.0.0.1:8088/health',timeout=5); sys.exit(0 if r.status==200 else 1)",
        ]
      interval: 60s
