          mv '/usr/lib/plexmediaserver/Plex Transcoder' '/usr/lib/plexmediaserver/Plex Transcoder.real'
        fi
        cp /spore/plex_transcoder_wrapper.sh '/usr/lib/plexmediaserver/Plex Transcoder'
        chmod +x '/usr/lib/plexmediaserver/Plex Transcoder'
        exec /init

  prowlarr:
    image: lscr.io/linuxserver/prowlarr:latest
    container_name: prowlarr
    restart: unless-stopped
    networks:
      - media-network
    ports:
      - "${PROWLARR_BIND:-0.0.0.0}:9696:9696"
    environment:
      PUID: ${PUID:-1000}
      PGID: ${PGID:-1000}
      TZ: ${TZ:-UTC}
    volumes:
      - ${CONFIG_DIR}/prowlarr:/config
    depends_on:
      byparr:
        condition: service_started

  byparr:
    image: ghcr.io/thephaseless/byparr:latest
    container_name: byparr
    restart: unless-stopped
    networks:
      - media-network
    ports:
      - "${BYPARR_BIND:-0.0.0.0}:8191:8191"
    environment:
      LOG_LEVEL: info
      LOG_HTML: "false"
      TZ: ${TZ:-UTC}

  radarr:
    image: lscr.io/linuxserver/radarr:latest
    container_name: radarr
    restart: unless-stopped
    networks:
      - media-network
    ports:
      - "${RADARR_BIND:-0.0.0.0}:7878:7878"
    environment:
      PUID: ${PUID:-1000}
      PGID: ${PGID:-1000}
      TZ: ${TZ:-UTC}
    volumes:
      - ${CONFIG_DIR}/radarr:/config

  sonarr:
    image: lscr.io/linuxserver/sonarr:latest
    container_name: sonarr
    restart: unless-stopped
    networks:
      - media-network
    ports:
      - "${SONARR_BIND:-0.0.0.0}:8989:8989"
    environment:
      PUID: ${PUID:-1000}
      PGID: ${PGID:-1000}
      TZ: ${TZ:-UTC}
    volumes:
      - ${CONFIG_DIR}/sonarr:/config

  seerr:
    image: ghcr.io/seerr-team/seerr:latest
    container_name: seerr
    restart: unless-stopped
    networks:
      - media-network
    ports:
      - "${SEERR_BIND:-0.0.0.0}:5055:5055"
    environment:
